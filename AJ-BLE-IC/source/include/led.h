#define LED1_ON 			(RA0 = 0x00) // 输出低电平
#define LED1_TOG 			(RA0 ^= 0x01) // 输出反电平

#define LED2_TRIS			(TRISAbits.TRISA1) // RA1
#define LED2_OFF 			(RA1 = 0x01) // 输出高电平
#define LED2_ON 			(RA1 = 0x00) // 输出低电平
#define LED2_TOG 			(RA1 ^= 0x01) // 输出反电平

