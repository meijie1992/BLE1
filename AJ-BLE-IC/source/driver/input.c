/******************************************************************************
** 函数名称 : init_input
** 输入参数 : 无
** 输出参数 : 无
** 功能描述 : 初始化输入信号
** 全局变量 : 无
** 作    者 : 宋志康
** 日    期 : 2016-08-23
** 修    改 : 无
** 版    本 : V0.3
*******************************************************************************/
void
init_input(
	void
	)
{
	INPUT1_TRIS = 0x01; // 设置RF7输入
	INPUT2_TRIS = 0x01; // 设置RF6输入
	INPUT3_TRIS = 0x01; // 设置RF5输入
	INPUT4_TRIS = 0x01; // 设置RF4输入
	INPUT5_TRIS = 0x01; // 设置RF3输入
	INPUT6_TRIS = 0x01; // 设置RF2输入
	INPUT7_TRIS = 0x01; // 设置RF1输入
	INPUT8_TRIS = 0x01; // 设置RF0输入
	FRD_INPUT_TRIS = 0x01; // 设置RE1输入
	SWITCH_INPUT_TRIS = 0x01; // 设置RE0输入
}
