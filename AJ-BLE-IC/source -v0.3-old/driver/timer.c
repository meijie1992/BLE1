//**--------------函数定义-----------------------------------------------------
void
init_timer1(
	void
	)
{
	TMR1H = 0xE0; // 16 位 TMR1 寄存器高字节的保持寄存器
	TMR1L = 0xC0; // 16 位 TMR1 寄存器低字节的保持寄存器 定时1ms
	T1CON = 0x41; // bit7-6 01 Timer1时钟源为系统时钟 
	              // bit0 1 使能 Timer1
	              // bit5-4 00 Timer1 输入时钟预分频比选择位 1:1
	              // 
	              
	TMR1IE = 0x01; // 使能定时器1中断
}
